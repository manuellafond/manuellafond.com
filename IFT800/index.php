          <div class="section_title">
            <div class="section_title_text">
              Quelques proposition de projet (FPT)
            </div>
          </div>
          
          <div style="font-weight:bold;">Problèmes théoriques ouverts</div>
          <ul>
            
            <li>
              Ma liste de problèmes ouverts peut servir d'inspiration aussi pour les algos FPT.  <br>
              Voir : <a href="https://mlafond.recherche.usherbrooke.ca/problems.php">https://mlafond.recherche.usherbrooke.ca/problems.php</a>
            </li>
            
            <li>
              Vous trouverez beaucoup de problèmes ouverts sur : <a href="http://fpt.wikidot.com/open-problems">http://fpt.wikidot.com/open-problems</a>.  Ils sont extrêmement difficiles, mais j'aurai moins d'attente si vous regardez un problème de cette envergure.
            </li>
            
            <li>
                Il y a des problèmes FPT en <b>informatique quantique</b>.  Si vous cherchez, vous allez trouver.  Voici deux exemples d'article:<br>
                <a href='https://journals.aps.org/prx/abstract/10.1103/PhysRevX.6.021043'>Trading Classical and Quantum Computational Resources</a><br>
                <a href='https://arxiv.org/pdf/2106.07740'>Improved upper bounds on the stabilizer rank of magic states</a>
            </li>
            
            
          </ul>
          
          
          <div style="font-weight:bold;">Projets pratiques</div>
          <ul>
            
            <li>
              Évaluez la pertinence de la kernelisation.  Par exemple, implémentez un algo de branchement et évaluez le temps.  Ensuite, implémentez un noyau et exécutez l'algo de branchement sur le noyau.  Est-ce qu'un gain de temps est observé?  Aussi, est-ce qu'on devrait appeler la procédure de noyau à chaque appel récursif, ou seulement au début?
            </li>
            <li>
              Prenez un problème, étudié en classe ou non, et implémentez 2 ou 3 algos FPT.  Évaluez la taille de données que vous pouvez tolérer, et comparez les temps d'exécution.  
              Bien sûr, il est préférable d'étudier un nouveau problème.
            </li>
          </ul>
          
          
          <div style="font-weight:bold;">Revue de littérature</div>
          Vous pouvez résumer l'état de l'art sur divers sujets.  Ceci demandera fouiller dans la littérature et de lire plusieurs articles.  Ne vous attardez pas trop aux détails des preuves, sinon vous ne finirez jamais!  Lisez les introductions, 
          faites l'extraction des idées principales des algorithmes et résumez-les.  Des propositions de sujets: 
          <ul>
            <li>
              Un récent article résume plusieurs algorithmes FPT utilisés en bioinformatique.  
              <a href="https://www.mdpi.com/1999-4893/12/12/256/pdf">L'article est ici</a>.
              Vous devez donc faire une revue d'une revue.  Pigez ce que vous trouvez pertinent dans l'article et vulgarisez.
            </li>
            <li>
              Il y a beaucoup de résultats sur VERTEX-COVER que nous n'avons pas vu en classe.  Cherchez d'autres résultats et résumez-les.  À ma connaissance, le meilleur algo pour VERTEX-COVER est <a href="https://www.cs.lafayette.edu/~gexia/research/mfcs06.pdf">dans cet article</a>.  
            </li>
            <li>
              Un article célèbre en FPT fait des connexions entre le FPT et les PTAS.  <a href="https://erikdemaine.org/papers/GenApprox_SODA2005/paper.pdf">L'article est ici</a>.
              Je n'ai jamais pris le temps de le lire, alors si vous le vulgarisez, j'apprendrai quelque chose.
