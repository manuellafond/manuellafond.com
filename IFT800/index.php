            <div class="section_title_text">
              Quelques proposition de projet (approximation)
            </div>
          </div>
          
          <div style="font-weight:bold;">Problèmes théoriques ouverts</div>
          <ul>
            <li>
                Il y a une liste de problèmes ouverts en informatique sur Wikipedia.  Voir: <a href='https://en.wikipedia.org/wiki/List_of_unsolved_problems_in_computer_science'>https://en.wikipedia.org/wiki/List_of_unsolved_problems_in_computer_science</a>. <br>
                Vous pouvez voir si un de ces problèmes peut être abordé sous l'angle de l'approximation.
                
            </li>
            <li>
                Si vous cherchez "open problems in approximation algorithms", vous trouverez rapidement des problèmes ouverts.
                Par exemple le premier lien que j'ai est: <a href='https://people.orie.cornell.edu/dpw/talks/ELTENov2010.pdf'>https://people.orie.cornell.edu/dpw/talks/ELTENov2010.pdf</a>, qui cite une série de problèmes (par contre il faut un moment pour les absorber).
                
            </li>
            <li>
              J'ai une liste de problèmes ouverts que j'ai rencontrés pendant mes recherches, si ça peut servir d'inspiration.  <br>
              Voir : <a href="https://mlafond.recherche.usherbrooke.ca/problems.php">https://mlafond.recherche.usherbrooke.ca/problems.php</a>
            </li>
            <li>
              Une approche possible est de tenter d'appliquer les méthodes vues en classe.  Pouvez-vous trouver une borne sur OPT?  Sinon, pourquoi?  
              Est-ce qu'une solution gloutonne ou de recherche locale fonctionne?  Pourquoi?  Peut-on écrire un programme linéaire?
            </li>
            
            <li>
              Vous trouverez beaucoup de problèmes ouverts sur : <a href="http://fpt.wikidot.com/open-problems">http://fpt.wikidot.com/open-problems</a>.  Ils sont extrêmement difficiles, mais j'aurai moins d'attente si vous regardez un problème de cette envergure.
            </li>
            
            
          </ul>
          
          
          <div style="font-weight:bold;">Projets pratiques</div>
          <ul>
            <li>
              Vous pouvez implémenter un algorithme d'approximation existant, incluant ceux vus en classe (mais bien sûr, il est recommandé de viser l'originalité).  
              Le but du projet serait ensuite d'évaluer la qualité de l'approximation trouvée.  Ceci vous demandera de concevoir une expérimentation dans laquelle OPT est connue pour vos instances, et de trouver le ratio de votre approximation.
            </li>
            <li>
              Vous pouvez implémenter plusieurs algorithmes d'approximation pour un même problème, puis comparer leurs temps d'exécution et qualité d'approximation.  Dans ce cas, on peut avoir des expériences intéressantes même sans connaître OPT.
            </li>
            <li>
              Vous pouvez évaluer la pertinence de créer une procédure post-approximation.  C'est-à-dire, implémentez un algorithme d'approximation, puis pensez à une façon d'améliorer cette solution (par ex, dans vertex-cover, enlever des sommets tant que ça demeure un vertex-cover).  Évaluez à quel point ça améliore.
            </li>
            <li>
              Autres idées bienvenues...
            </li>
            
            
          </ul>
          
          
          <div style="font-weight:bold;">Revue de littérature</div>
          Vous pouvez résumer l'état de l'art sur divers sujets.  Ceci demandera fouiller dans la littérature et de lire plusieurs articles.  Ne vous attardez pas trop aux détails des preuves, sinon vous ne finirez jamais!  Lisez les introductions, 
          faites l'extraction des idées principales des algorithmes et résumez-les.  Des propositions de sujets: 
          <ul>
            
        <li>
            Le texte ici survole les approximations possibles avec la technique de recherche locale: <a href='http://ndl.ethernet.edu.et/bitstream/123456789/47033/1/Evripidis%20Bampis_2006.pdf#page=36'>http://ndl.ethernet.edu.et/bitstream/123456789/47033/1/Evripidis%20Bampis_2006.pdf#page=36</a>.  En utilisant ce texte comme point de départ, résumez les résultats dans le domaine.
            
        </li>
        <li>
              Les problèmes de clustering.   Deux problèmes sont populaires: CLUSTER-EDITING et CORRELATION CLUSTERING.  <br>
              Voir <a href='https://erikdemaine.org/papers/Clustering_TCS/paper.pdf'>cet article pour démarrer</a>.
            </li>
            <li>
              Le problème de la plus courte super-séquence (shortest superstring).  <br>
              Voir <a href='https://www.mimuw.edu.pl/~mucha/teaching/aa2008/ss.pdf'>ce tutoriel</a> pour y voir plusieurs références.
            </li>
            <li>
              Il y a toute une historique d'algorithmes d'approximation pour Min-TSP et Max-TSP (TSP = traveling salesperson).  Vous devriez pouvoir trouver des références facilement.
            </li>
            <li>
              SET-COVER et ses variantes.  <br>
              Par exemple, <a href='https://en.wikipedia.org/wiki/Maximum_coverage_problem'>la couverture maximum</a>, ou encore le <a href='https://arxiv.org/abs/1907.04413'>partial set cover</a>.
            </li>
            <li>
              Les problèmes de <i>scheduling</i>.  Il y a beaucoup de variantes.  <br>
              Comme point de départ, je vous propose <a href='http://i.stanford.edu/pub/cstr/reports/cs/tr/98/1611/CS-TR-98-1611.pdf'>une thèse qui en explique beaucoup</a>.
            </li>
            
            <li>
              Il y a aussi plusieurs articles de type "survey".  Ils sont souvent trop longs, mais l'idée serait de choisir un ensemble de résultats à présenter.  
              Par exemple:<br>
              <a href='https://www.wisdom.weizmann.ac.il/~oded/bbfr.pdf'>Local ratio survey</a><br>
